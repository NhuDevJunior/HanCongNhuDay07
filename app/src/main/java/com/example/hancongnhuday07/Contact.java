package com.example.hancongnhuday07;

public class Contact {
    String name;
    String mess;


    public Contact(String name, String mess) {
        this.name = name;
        this.mess = mess;
    }

    public String getName() {
        return name;
    }

    public String getMess() {
        return mess;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setMess(String mess) {
        this.mess = mess;
    }

    }

